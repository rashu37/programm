public class Multipleofsix{
   public static void main(String args[]){
   
   int elements[]={7,6,8,18,23,30,36,45,34};
   for(int index=0; index<elements.length; index++){
      if(elements[index] %6==0){
	  System.out.println("the number is multiple of six:" + elements[index]);
	  }else{
	  System.out.println("the number is not multiple of six:" + elements[index]);
	  }
	  }
	  
	  }
	  }